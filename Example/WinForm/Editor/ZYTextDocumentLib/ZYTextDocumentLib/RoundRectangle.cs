namespace ZYTextDocumentLib
{
	public class RoundRectangle
	{
		private int intLeft = 0;

		private int intTop = 0;

		private int intWidth = 0;

		private int intHeight = 0;

		private int intRadius = 0;

		public int Left
		{
			get
			{
				return intLeft;
			}
			set
			{
				intLeft = value;
			}
		}

		public int Top
		{
			get
			{
				return intTop;
			}
			set
			{
				intTop = value;
			}
		}

		public int Width
		{
			get
			{
				return intWidth;
			}
			set
			{
				intWidth = value;
			}
		}

		public int Height
		{
			get
			{
				return intHeight;
			}
			set
			{
				intHeight = value;
			}
		}

		public int Radius
		{
			get
			{
				return intRadius;
			}
			set
			{
				intRadius = value;
			}
		}
	}
}
